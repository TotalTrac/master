CREATE PROCEDURE [StVincent].[CarrierRouteGetByID]
	@ID int
AS
BEGIN
	SET NOCOUNT ON;
	
	SELECT
		CRRT_ID
		, CRRT_AVERAGEAGE
		, CRRT_BOXES
		, CRRT_BUSINESS
		, CRRT_CRRT
		, CRRT_INCOME
		, CRRT_LATITUDE
		, CRRT_LONGITUDE
		, CRRT_MFDU
		, CRRT_MKTMED
		, CRRT_PERCENTCHILD
		, CRRT_SFDU
		, CRRT_TRLR
		, CONVERT(varbinary(MAX), CRRT_WKB) AS [CRRT_WKB]
		, CRRT_WKT		
		, CRRT_ZIPCODEID		
	FROM
		StVincent.[CarrierRoutes]
	WHERE
		CRRT_ID = @ID
END
GO