CREATE PROCEDURE [dbo].[colpenn_RejectRecordsGetPrintKeyCountsByMailDateRange] (
  @DateFrom	datetime	= NULL
  , @DateTo		datetime	= NULL  
)
AS
BEGIN
	SET NOCOUNT ON;
	
	SELECT
		REJECTRECORD_PRINTKEY AS PRINTKEY
		, NULL AS PRINTKEY_EFFORT
		, COUNT(*) AS PRINTKEY_COUNT
	FROM 
		[dbo].[colpenn_RejectRecords] 
	WHERE
		(@DateFrom IS NULL OR REJECTRECORD_MAILDATE >= @DateFrom)
		AND (@DateTo IS NULL OR REJECTRECORD_MAILDATE <= @DateTo)
	GROUP BY
		REJECTRECORD_PRINTKEY
END
GO