CREATE PROCEDURE [file].[FileGetByID] 
	@ID	int
AS
BEGIN
	SET NOCOUNT ON;

	SELECT
		[FILE_ID]
		, FILE_COMPRESSED
		, FILE_CONTENTTYPEID		
		, FILE_DESCRIPTION
		, [FILE_NAME]
		, FILE_FOLDERID
		, FILE_SIZE
		, FILE_TYPEID
		, FILE_UPLOADED
		, FILE_USERID
		, FILE_ROWVERSION	
	FROM 
		[file].[Files]
	WHERE 
		[FILE_ID] = @ID;
END
GO