CREATE PROCEDURE [file].[FileLinkGetByID]
	@ID	int
AS
BEGIN
	SET NOCOUNT ON;
	
	SELECT
		LINK_ID
		, LINK_CREATED
		, LINK_CREATEDBYID
		, LINK_EMAILADDRESS
		, LINK_FILEID
		, LINK_KEY		
	FROM
		[file].[FileLinks]
	WHERE
		LINK_ID = @ID;
END
GO