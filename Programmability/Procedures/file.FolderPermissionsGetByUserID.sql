CREATE PROCEDURE [file].[FolderPermissionsGetByUserID]
	@ID	int
AS
BEGIN
	SET NOCOUNT ON;

    SELECT
		FOLDERPERMISSION_ID
		, FOLDERPERMISSION_FOLDERID
		, FOLDERPERMISSION_PRIVILEGES
		, FOLDERPERMISSION_USERID
		, FOLDERPERMISSION_ROWVERSION
	FROM
		[file].[FolderPermissions]
	WHERE
		FOLDERPERMISSION_USERID = @ID;
END
GO