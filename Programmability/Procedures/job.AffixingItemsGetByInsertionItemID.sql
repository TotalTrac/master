CREATE PROCEDURE [job].[AffixingItemsGetByInsertionItemID]
	@ID int
AS
BEGIN

	SET NOCOUNT ON;

	SELECT 
		ID
		, AFFIXINGLOCATIONTYPEID
		, [DESCRIPTION]
		, INSERTITEMID
		, INVENTORYITEMID
		, SIZE
		, StampTypeId
    , StartTime
    , EndTime
		, TABBEDEDGES
		, TABS
		, TABTYPEID
		, TYPEID
		, UP
		, XBASELINE
		, XOFFSET
		, YBASELINE
		, YOFFSET
		, [ROWVERSION]
	FROM 
		job.[AffixingItems]
	WHERE 
		INSERTITEMID = @ID;
END
GO