CREATE PROCEDURE [job].[PunchesGetOpenByJobID]	
	@JobID	int
AS
BEGIN
	SET NOCOUNT ON;
	
	SELECT
		ID		
		, JOBID
		, MACHINENAME
		, STARTTIME
		, STOPTIME
		, USERNAME
		, [ROWVERSION]
	FROM
		[job].[Punches]
	WHERE
		JOBID = @JobID
		AND STOPTIME IS NULL;
	
END
GO