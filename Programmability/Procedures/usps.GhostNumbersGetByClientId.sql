CREATE PROCEDURE [usps].[GhostNumbersGetByClientId] 
	@ID int
AS
BEGIN
	
	SELECT
		GHOSTNUMBER_ID
		, GHOSTNUMBER_CLIENTID
		, GHOSTNUMBER_NUMBER
		, GHOSTNUMBER_PERMITID
	FROM
		[usps].[GhostNumbers] 
	WHERE 
		GHOSTNUMBER_CLIENTID = @ID;

END
GO