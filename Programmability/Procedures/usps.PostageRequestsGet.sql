CREATE PROCEDURE [usps].[PostageRequestsGet]
	
AS
BEGIN
	
	SET NOCOUNT ON;

	SELECT
		ID
		, AMOUNTREQUESTED
		, CLIENTID
		, EXPECTED		
		, MAILINGID
		, NOTES
		, REQUESTED
		, REQUESTEDBYID
		, [ROWVERSION]
	FROM 
		[usps].[PostageRequests] 
	ORDER BY
		ID;
END
GO